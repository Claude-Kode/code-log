// REGISTER_REFLECT()

class SGT {
  std::vector<int> seg;
  int off = 0;
  void pull(int i) { seg[i] = seg[i << 1] + seg[i << 1 | 1]; }   
 public:
  SGT() = delete;
  SGT(int n) {
    off = std::bit_ceil(unsigned(n + 2));
    seg.assign(off << 1, 0);
  }
  void add(int i) {            
    seg[i += off]++;
    while (i >>= 1) pull(i);
  }
  int ask(int l, int r) {             
    if (l > r) return 0;
    int res = 0;
    for (l += off - 1, r += off + 1; l ^ r ^ 1; l >>= 1, r >>= 1) {
      if (~l & 1) res += seg[l ^ 1];
      if (r & 1)  res += seg[r ^ 1];
    }
    return res;
  }
};

void solve() {
  int n; cin >> n;
  vector<int> a(n + 1);
  for (int i = 1; i <= n; i ++) {
    cin >> a[i];
  }

  map<int, int> L, R;
  map<int, vector<int>> pos;
  for (int i = 1; i <= n; i ++) {
    R[a[i]] = i; 
    pos[a[i]].push_back(i);
  }

  for (int i = n; i >= 1; i --) {
    L[a[i]] = i; 
  }

  auto v = a;
  sort(v.rbegin(), v.rend() - 1);
  v.erase(unique(v.begin(), v.end()), v.end());
  // dbug(v);
  
  SGT seg(n + 1);

  L[0] = 1; R[0] = 1;
  // 区间内 已经被删掉的元素数量
  auto del = [&](int l, int r) -> int { return seg.ask(l, r); };

  vector<array<int, 2>> dp(v.size(), {INT_MAX, INT_MAX});
  dp[0] = {0, 0};

  for (int i = 1; i < (int)dp.size(); i++) {
    auto& P = pos[v[i]];
    int cl = L[v[i]], cr = R[v[i]], k = (int)P.size();

    // lst_l -> L -> R
    // lst_l -> R -> L
    // lst_r -> L -> R
    // lst_r -> R -> L

    // 这个区间有多少这次要删除的元素
    auto pc = [&](int l, int r) -> int {
      // [l, r)
      return (int)(upper_bound(P.begin(), P.end(), r)
                 - lower_bound(P.begin(), P.end(), l));
    };

    auto T = [&](int b1, int b2) -> int {
      int lo = min(b1, b2), hi = max(b1, b2);
      if (lo == hi) return 0;
      return (hi - lo) - del(lo + 1, hi) - pc(lo + 1, hi);
    };

    // 除掉 之前的删掉的数字, 和这次要删掉的数字, 从 cl 到 cr 还剩下多少数字
    int sweep = (cr - cl + 1) - del(cl, cr) - k;
    int pr = (i == 1) ? 0 : R[v[i - 1]];
    int pl = (i == 1) ? 0 : L[v[i - 1]] - 1;

    // lst_d -> cur_d ^ 1 -> cur_d
    // 等于 走完 [L, R] 的完整的没删的 + lst_d -> cur_d ^ 1 的删掉的
    // 也就是 T(lst_d, cur_(d ^ 1)) + sweep
 
    // 用每个边界的左边的数字代替这个边界
    // 假如边界两段都是要删除的元素, 操作就是从 r 的右端移动到 l 的右端, 再加上删掉 l 的操作
    

    dp[i][0] = min(dp[i - 1][0] + T(pr, cl - 1), dp[i - 1][1] + T(pl, cl - 1)) + sweep;
    dp[i][1] = min(dp[i - 1][0] + T(pr, cr),     dp[i - 1][1] + T(pl, cr)) + sweep;

    for (int& p : P) seg.add(p);
  }

  cout << n + min(dp.back()[0], dp.back()[1]) << "\n";
} 

int32_t main() {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);
  std::cout.tie(nullptr);
  
  int tt = 1, _ = 0;
  std::cin >> tt;
  while (tt-- and ++_)
    // std::cout << "TEST CASE : " << _ << endl,
    solve();

  return 0;
}

